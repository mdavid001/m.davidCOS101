fn main() {
    //addition
    let sum = 5500 + 7310;
    println!("The sum is {}", sum);

    //subtraction
    let difference:f32 = 95.5 - 4.3;
    println!("The difference of 95.5 and 4.3 is {}",difference );

    //multiplication
    let product:i32 = 4 * 30;
    println!("The multiple of 4 and 30 is {}",product );

    //division
    let quotient:f64 = 56.7 / 32.2;
    println!("The division of 56.7 and 32.3 is {}",quotient );

    //remainder
    let remainder = 43 % 5;
    println!("The remainder of 43 and 5 is {}",remainder );
}
